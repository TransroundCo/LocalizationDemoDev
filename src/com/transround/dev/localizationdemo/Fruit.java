package com.transround.dev.localizationdemo;

public class Fruit {
	String mName;
	double mCalories;
	double mProtein;
	double mCarbohydrate;
	double mFibre;
	double mDailyValue;

	public Fruit() {
		super();
	}

	public String getName() {
		return mName;
	}

	public void setName(String mName) {
		this.mName = mName;
	}

	public double getCalories() {
		return mCalories;
	}

	public void setCalories(double mCalories) {
		this.mCalories = mCalories;
	}

	public double getProtein() {
		return mProtein;
	}

	public void setProtein(double mProtein) {
		this.mProtein = mProtein;
	}

	public double getCarbohydrate() {
		return mCarbohydrate;
	}

	public void setCarbohydrate(double mCarbohydrate) {
		this.mCarbohydrate = mCarbohydrate;
	}

	public double getFibre() {
		return mFibre;
	}

	public void setFibre(double mFibre) {
		this.mFibre = mFibre;
	}

	public double getDailyValue() {
		return mDailyValue;
	}

	public void setDailyValue(double mDailyValue) {
		this.mDailyValue = mDailyValue;
	}

}
